        String ebookTitle = "Пролог";
        // размер
        int size = 16;
        // вместимость (чего-либо)
        int capacity = 32;
        // счетчик
        int counter = 0;
        // путь до файла
        String filePath = "C:\\User\\Documents\\plan.txt";
        // количество чисел в каждой строке
        int amountNumbersEachRow = 0;

        System.out.println("Цифра = " + digit);
        System.out.println("Сумма чисел = " + sumNumbers);
        System.out.println("Произведение цифр = " + productDigits);
        System.out.println("Максимальное число = " + maxNumber);
        System.out.println("Количество десятков = " + quantityTens);
        System.out.println("Вес собаки - " + weightDog + " кг");
